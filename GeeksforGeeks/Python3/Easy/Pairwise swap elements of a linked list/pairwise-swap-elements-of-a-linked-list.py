# return head of list after swapping
class Solution:    
    def pairWiseSwap(self, head):
        # code here
        i=0
        curr=head
        prev=None
        while curr and i<2:
            tmp=curr.next
            curr.next=prev
            prev=curr
            curr=tmp
            i+=1
        i=0
        if tmp!=None:
            head.next=self.pairWiseSwap(tmp)
        return prev

